package de.rapha149.displayutils.display.hologram;

/**
 * This enum represents the vertical alignment options for a hologram.
 */
public enum HologramVerticalAlignment {
     // Represents the top alignment for a hologram, i.e. the first line will be directly below the hologram's location
     TOP,

     // Represents the center alignment for a hologram, i.e. the hologram will be centered around the hologram's location
     CENTER,

     // Represents the bottom alignment for a hologram, i.e. the last line will be diretly above the hologram's location
     BOTTOM
}